#include "car_motion.h"

//-------------------------------------------------------------------------------------------------------------------
// @brief        智能车前进
// @param        void    
// @return       void
// Sample usage:        car_forward(); 
                        //智能车前进
//-----------------------------------------------------------------------------------------------------------------
void car_forward()
{
    l9110s_forward(left,10000);
    l9110s_forward(right,10000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        智能车后退
// @param        void    
// @return       void
// Sample usage:        car_back(); 
                        //智能车后退
//-----------------------------------------------------------------------------------------------------------------
void car_back()
{
    l9110s_back(left,10000);
    l9110s_back(right,10000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        智能车停止
// @param        void    
// @return       void
// Sample usage:        car_stop(); 
                        //智能车停止
//-----------------------------------------------------------------------------------------------------------------
void car_stop()
{
    l9110s_forward(left,0);
    l9110s_forward(right,0);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        智能车左转
// @param        void    
// @return       void
// Sample usage:        car_turnleft(); 
                        //智能车左转
//-----------------------------------------------------------------------------------------------------------------
void car_turnleft()
{
    l9110s_forward(left,5000);
    l9110s_forward(right,10000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        智能车右转
// @param        void    
// @return       void
// Sample usage:        car_turnright(); 
                        //智能车右转
//-----------------------------------------------------------------------------------------------------------------
void car_turnright()
{
    l9110s_forward(left,10000);
    l9110s_forward(right,5000);
}
