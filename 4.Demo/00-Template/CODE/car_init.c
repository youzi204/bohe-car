#include "car_init.h"

//-------------------------------------------------------------------------------------------------------------------
// @brief        智能车初始化
// @param        void    
// @return       void
// Sample usage:        car_init(); 
                        //初始化智能车
//-------------------------------------------------------------------------------------------------------------------
void car_init()
{
	l9110s_init(left);
	l9110s_init(right);
    ws2812b_init();
    ultrasonic_init(A4, A0);
    tm1637_init();
    gpio_init(A6, GPO, 1, SPEED_50MHZ|OUT_PP);                //初始化蜂鸣器引脚
}	
